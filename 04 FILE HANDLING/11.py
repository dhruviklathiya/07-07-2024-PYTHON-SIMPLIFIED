# r+ mode in python

# read mode => Read file
# write mode => Write file (but with replacement)
# append mode => Write file (adding data)
# r+ mode => Read file & can overwrite (It does not replace whole data but overwrite the data)

# read mode => No auto generation of file
# write mode => Auto generation of file
# append mode => Auto generation of file
# r+ mode => No auto generation of file