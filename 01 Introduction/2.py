# Auto calculation of python just like javascript
print(4+4)
print(4*3)

# Everything else is same except for division
    # By default python returns float value in single division operator
print(4/2)
print(2/4)
    # We can make them integer by using double division operator || integer division
print(4//2)
print(2//4)

# Power in python
print(2**3) # Stands for 2^3 => 2 * 2 * 2

# Find SQUARE ROOT
print(2**0.5)

# Get certain floating points
print(round(2**0.5, 2))

