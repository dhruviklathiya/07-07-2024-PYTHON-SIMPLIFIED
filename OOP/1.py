# Object-Oriented Programming (OOP) is a programming paradigm that uses objects and classes as its core components. It is designed to facilitate code reuse, modularity, and the organization of software by modeling real-world entities and their interactions.

# Object-oriented programming language is a programming language that follows all 4 pillars of OOP:
    # Inheritance
    # Polymorphism
    # Data abstraction || Abstraction
    # Encapsulation
